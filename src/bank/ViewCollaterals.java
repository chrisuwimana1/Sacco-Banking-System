    private void myCollaterals(String contractId) {

        DefaultTableModel dtm = (DefaultTableModel) listcollaterals.getModel();
        dtm.setRowCount(0);
        dtm.setColumnCount(0);

        try {
            String sql = "Select Collateral_Type,Collateral_Amount_LCY,Collateral_Discount_Amt_LCY,Collateral_market_value,Collateral_Last_Valuation_Date,Collateral_Expiry_Date from collaterals where Contract_ID = ?";
            pst = conn.connection.prepareStatement(sql);
            pst.setString(1, contractId);
            rs = pst.executeQuery();
            //int count = 0;
            listcollaterals.setModel(model);
            model.addColumn("Type");
            model.addColumn("Amount");
            model.addColumn("Discounted Value");
            model.addColumn("Market Value");
            model.addColumn("Last Valuation Date");
            model.addColumn("Expiry Date");

            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("Collateral_Type"), rs.getString("Collateral_Amount_LCY"),
                    rs.getString("Collateral_Discount_Amt_LCY"), rs.getString("Collateral_market_value"),
                    rs.getString("Collateral_Last_Valuation_Date"), rs.getString("Collateral_Expiry_Date")
                });
            }

        } catch (SQLException ex) {
            // TODO Auto-generated catch block
            JOptionPane.showMessageDialog(null, ex);
        }

    }

