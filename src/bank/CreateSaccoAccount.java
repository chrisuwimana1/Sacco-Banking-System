    private void CreateSaccoAccount() {
        java.util.Date expenseDate = created_date.getDate();
        java.sql.Date sqlCreatedDate = new java.sql.Date(expenseDate.getTime());

        String sql = "INSERT into sacco_accounts (account_number,account_name, bank,amount, date_created) values (?,?,?,?,?)";
        try {
            pst = conn.connection.prepareStatement(sql);
            pst.setString(1, account_number.getText());
            pst.setString(2, account_name.getText());
            pst.setString(3, bank.getText());
            pst.setFloat(4, Float.parseFloat(amount.getText()));
            pst.setString(5, sqlCreatedDate.toString());
            pst.execute();
            JOptionPane.showMessageDialog(null, "The Sacco Bank Account has been Created");
            this.dispose();
        } catch (SQLException e1) {
            JOptionPane.showMessageDialog(null, e1);
        }
    }

