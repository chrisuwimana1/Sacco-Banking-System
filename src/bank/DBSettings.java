    private void saveDbSettings() {
        if (ip_address.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "The IP Address is required !", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            //save to localstorage
            prefs.put("host_name", ip_address.getText());
            prefs.put("db_name", db_name.getText());
            prefs.put("db_username", db_username.getText());
            prefs.put("db_password", DB_PASSWORD.getText());
            
            JOptionPane.showMessageDialog(null, "Saved", "Success", JOptionPane.INFORMATION_MESSAGE);
            
            //logout and sign in again
            new Login().setVisible(true);
            this.dispose();
        }
    }
