    private void addNewRequestee() {
        java.sql.Timestamp currentDate = new java.sql.Timestamp(new java.util.Date().getTime());
        String sql = "INSERT INTO `requestee`(`id`, `firstname`, `lastname`, `date_created`) VALUES (0,?,?,?)";
        try {
            pst = conn.connection.prepareStatement(sql);
            pst.setString(1, firstname.getText());
            pst.setString(2, lastname.getText());
            pst.setTimestamp(3, currentDate);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Requestee has been Created");
            this.dispose();
            new AddExpense().setVisible(true);
        } catch (SQLException e1) {
            JOptionPane.showMessageDialog(null, e1);
        }
    }
