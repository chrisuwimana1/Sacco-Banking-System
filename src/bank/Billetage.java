    private void filterData() {
        if (transaction_date.getDate() == null) {
            JOptionPane.showMessageDialog(null, "The specific date is required for this filter!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            java.util.Date specificDate = transaction_date.getDate();
            java.sql.Date specificDateSql = new java.sql.Date(specificDate.getTime());
            String transactionDate = specificDateSql.toString();

            try {
                DefaultTableModel dtm = (DefaultTableModel) list_of_transactions.getModel();
                dtm.setRowCount(0);
                dtm.setColumnCount(0);
                String sql = null;                  

                sql = "SELECT t.transaction_amount, t.transaction_net_amount, billetage.`account_number`, `5000`, `2000`, `1000`, `500`, `100`, `50`, `20`, `10`, `5`, `1`, t.date_created FROM `billetage` LEFT JOIN transactions t on t.transaction_id = billetage.transaction_id WHERE t.date_created LIKE ? ORDER BY t.date_created DESC";
                    pst = conn.connection.prepareStatement(sql);
                    pst.setString(1, transactionDate);
                    rs = pst.executeQuery();
                    list_of_transactions.setModel(model);
                    model.addColumn("Account Number");
                    model.addColumn("Transaction Amount");
                    model.addColumn("5000");
                    model.addColumn("2000");
                    model.addColumn("1000");
                    model.addColumn("500");
                    model.addColumn("100");
                    model.addColumn("50");
                    model.addColumn("20");
                    model.addColumn("10");
                    model.addColumn("5");
                    model.addColumn("1");
                    model.addColumn("Date");

                    while (rs.next()) {
                        model.addRow(new Object[]{rs.getString("Account_Number"), 
                            formatFloat(rs.getString("transaction_amount")),
                            formatFloat(rs.getString("5000")), 
                            formatFloat(rs.getString("2000")),
                            formatFloat(rs.getString("1000")), 
                            rs.getInt("5000"), 
                            rs.getInt("2000"),
                            rs.getInt("1000"), 
                            rs.getString("500"),
                            rs.getString("100"), 
                            rs.getString("50"),
                            rs.getString("20"),
                            rs.getString("10"),
                            rs.getString("5"),
                            rs.getString("1"),
                            rs.getString("date_created")
                        });
                    }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }

