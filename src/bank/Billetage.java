    public void exportTable() {
        try {
         String fileName = "Sacco/";
         String directoryName = FileSystemView.getFileSystemView().getDefaultDirectory().getPath()+"/Documents/".concat(fileName);
         File dir = new File(directoryName);
          if (!dir.exists()) dir.mkdirs();

         ExcelExporter exp = new ExcelExporter();
         exp.exportTable(list_of_transactions, new File(directoryName+"_BILLETAGE.xls"));

         //OPEN FILE
         File tmpDir = new File(directoryName+"_BILLETAGE.xls");
         boolean exists = tmpDir.exists();
           if (exists) {
               Desktop dt = Desktop.getDesktop();
               dt.open(new File(directoryName+"_BILLETAGE.xls"));
           }
       } catch (IOException ex) {
         Logger.getLogger(Transaction.class.getName()).log(Level.SEVERE, null, ex);
       }
    }
